package com.example.numad23sp_huiyingchen;

public class CardObject{

    private final String name;
    private final String url;

    public CardObject(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }
    public String getUrl() {
        return url;
    }
}
